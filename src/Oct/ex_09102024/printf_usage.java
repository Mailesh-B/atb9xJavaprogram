package Oct.ex_09102024;

public class printf_usage {
    public static void main(String[] args) {
        {
            int name1 = 10;
            char name2 = 'D';
            String name3 = "Mailesh";
            boolean name4 = false;
            System.out.printf("My name is  %d", name1);
            System.out.println();
            System.out.printf("His name is %c",name2);
            System.out.println();
            System.out.printf("The name is %s",name3);
            System.out.println();
            System.out.printf("The name is %b",name4);
        }
    }
}
